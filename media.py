
#Faça um programa em Python que receba quatro notas, calcule e imprima a média aritmética. 

nota01 = input("Digite a primeira nota: ")
nota02 = input("Digite a segunda nota: ")
nota03 = input("Digite a terceira nota: ")
nota04 = input("Digite a quarta nota: ")

n1 = int(nota01)
n2 = int(nota02)
n3 = int(nota03)
n4 = int(nota04)

média = ((n1+n2+n3+n4)/4)

print("A média aritimética é", média)
